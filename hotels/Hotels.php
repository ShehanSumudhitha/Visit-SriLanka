    <p class="sub-para">According to Sri Lanka Tourism Development Authority (SLTDA), there are 156 classified tourist hotels and 239 unclassified tourist hotels in Sri Lanka. Additionally, there are 36 boutique hotels, 46 boutique villas and 493 bungalows in the country.
        The classified hotels collectively have a capacity of 14,232 rooms and include 25 five-star hotels and 24 four-star hotels. SLTDA's grading is based on the criteria set by the World Tourism Organization. In 2011, the classified hotels accounted 
        for 70% of the total room capacity in the industry. A number of accommodation facilities that built during the British colonial era have been converted to high-class hotels later. These include the Galle Face Hotel, the Grand Oriental Hotel,
       the Mount Lavinia Hotel, the Queen's Hotel, Kandy, the Grand Hotel and St. Andrew's Hotel and the New Oriental Hotel.
